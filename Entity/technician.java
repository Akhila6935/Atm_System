package AtmSystem.Entity;

public class technician {
	
	private int tech_id;
	private String teach_name;
	private int tech_contact;
	private String tech_address;
	//getters and setters
	public int getTech_id() {
		return tech_id;
	}
	public void setTech_id(int tech_id) {
		this.tech_id = tech_id;
	}
	public String getTeach_name() {
		return teach_name;
	}
	public void setTeach_name(String teach_name) {
		this.teach_name = teach_name;
	}
	public int getTech_contact() {
		return tech_contact;
	}
	public void setTech_contact(int tech_contact) {
		this.tech_contact = tech_contact;
	}
	public String getTech_address() {
		return tech_address;
	}
	public void setTech_address(String tech_address) {
		this.tech_address = tech_address;
	}

}
